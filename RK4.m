%--------------------------------------------------------------------------
%
% RK4 implementation
%
%--------------------------------------------------------------------------
function [y] = RK4(func, t, y_0, h)
    k1 = func(t    , y_0         );
    k2 = func(t+h/2, y_0+(h/2)*k1);
    k3 = func(t+h/2, y_0+(h/2)*k2);
    k4 = func(t+h  , y_0+h*k3    );
    y = y_0 + (h/6)*(k1 + 2*k2 + 2*k3 + k4);
end